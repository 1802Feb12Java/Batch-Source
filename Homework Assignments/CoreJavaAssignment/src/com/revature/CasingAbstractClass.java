package com.revature;

public abstract class CasingAbstractClass {
	/*
	 * The purpose of this class is to provide a framework for a casing class.
	 * Allows for reusability and overriding of these abstract methods.
	 */

	public abstract Boolean checkingForUpperCase();
	public abstract String makeUpperCase();
	public abstract int stringToIntPlus();
	
	
}//end class

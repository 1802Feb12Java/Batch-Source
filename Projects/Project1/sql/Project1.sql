  CREATE OR REPLACE EDITIONABLE PROCEDURE "APPROVE_DENY" 
(
  REQ_ID IN NUMBER 
, APP_OR_DEN IN NUMBER 
, APPROVER IN VARCHAR2
) AS 
BEGIN
  UPDATE ERS_REIMBURSEMENTS
  SET
    RS_STATUS=APP_OR_DEN,
    R_RESOLVED=(SELECT CURRENT_TIMESTAMP FROM DUAL),
    U_ID_RESOLVER=(SELECT U_ID FROM ERS_USERS WHERE U_USERNAME=APPROVER)
  WHERE R_ID=REQ_ID;
  COMMIT;
END APPROVE_DENY;


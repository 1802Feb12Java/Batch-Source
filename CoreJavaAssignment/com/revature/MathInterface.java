package com.revature;

public interface MathInterface 
{
	static double addNum(double a, double b)
	{
		return a+b;
	}
	
	static double subtractNum(double a, double b)
	{
		return a-b;
	}
	
	static double multiplyNum(double a, double b)
	{
		return a*b;
	}
	
	static double divideNum(double a, double b)
	{
		return a/b;
	}
}
